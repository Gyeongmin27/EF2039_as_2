// 단어를 난이도에 맞게 필터링하는 함수
function getWordsByDifficulty(difficulty) {
    return wordDatabase[difficulty] || wordDatabase['중'];
}

// 랜덤으로 단어 선택 (2-3글자)
function getRandomWord(difficulty) {
    const words = getWordsByDifficulty(difficulty);
    const filteredWords = words.filter(word => word.length >= 2 && word.length <= 3);
    
    if (filteredWords.length === 0) {
        // 필터링된 단어가 없으면 전체에서 선택
        const randomIndex = Math.floor(Math.random() * words.length);
        return words[randomIndex];
    }
    
    const randomIndex = Math.floor(Math.random() * filteredWords.length);
    return filteredWords[randomIndex];
}

// 단어를 글자 배열로 분리
function splitWord(word) {
    return word.split('');
}

